// Generator for examples/iceberg-catalog-dashboard.json (BI persona) and
// examples/iceberg-dba-dashboard.json (DBA persona) — the dashboard halves of
// the Iceberg Catalog Explorer (docs/ICEBERG-CATALOG-EXPLORER-DEMO.md).
//
// Every tile reads the cross-catalog union layer `ice_meta_all.*` (installed
// by examples/iceberg-install.json), never a per-catalog database, so one
// shipped file works for any number of catalogs. Each tile carries the same
// optional filter block `/*[ AND catalog = {catalog:String} ]*/`, so the
// Dashboard's global filter bar renders ONE Catalog field driving every tile
// (blank = all catalogs). Per-table drill-down tiles (data_files /
// parquet_stats) are NOT here — database names can't be query parameters, so
// those ship via the install file's "Generate: per-catalog drill-down
// Library" entry instead.
//
// Why a generator: chart tiles restore their saved axes only when
// `panel.key` exactly equals schemaKey(resultColumns) = "name:type|…"; a
// wrong key silently re-derives axes (worse than none), so keys are derived
// live from the real cluster through FORMAT JSON — same rationale as
// build-system-explorer-charts.mjs.
//
// Run:  node examples/build-iceberg-dashboards.mjs
//       (needs the ice_meta_all views installed; ICE_CH_CMD overrides the
//        client command, default: $HOME/bin/cl cw-metrics)
// Out:  examples/iceberg-catalog-dashboard.json, examples/iceberg-dba-dashboard.json

import { execFileSync } from 'node:child_process';
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';
import { homedir } from 'node:os';

const here = dirname(fileURLToPath(import.meta.url));
const CH_CMD = (process.env.ICE_CH_CMD || `${homedir()}/bin/cl cw-metrics`).split(' ');

// Throwaway values so DESCRIBE can resolve {from}/{to}-parameterized tiles;
// never shipped — the output JSON keeps the placeholders unbound.
const TEST_FROM = '2026-01-01 00:00:00';
const TEST_TO = '2026-12-31 00:00:00';

const ch = (query) =>
  execFileSync(CH_CMD[0], [
    ...CH_CMD.slice(1),
    '--param_from', TEST_FROM,
    '--param_to', TEST_TO,
    '--query', query,
  ], { encoding: 'utf8', maxBuffer: 64 * 1024 * 1024 });

function schemaKey(sql) {
  const out = JSON.parse(ch(`SELECT * FROM (${sql}) LIMIT 1 FORMAT JSON`));
  return out.meta.map((m) => `${m.name}:${m.type}`).join('|');
}


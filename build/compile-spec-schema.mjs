// Deterministically compile the canonical query.spec JSON Schema into
// self-contained browser ESM. Ajv is a dev dependency only; the production
// bundle imports this generated validator and never ships the general engine.

import Ajv2020 from 'ajv/dist/2020.js';
import standaloneCode from 'ajv/dist/standalone/index.js';
import { build } from 'esbuild';
import { mkdir, readFile, writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const here = dirname(fileURLToPath(import.meta.url));
const root = resolve(here, '..');
const schemaPath = resolve(root, 'schemas/query-spec-v1.schema.json');
const generatedDir = resolve(root, 'src/generated');
const validatorPath = resolve(generatedDir, 'query-spec-v1-validator.js');
const schemaModulePath = resolve(generatedDir, 'query-spec-v1-schema.js');

const ANNOTATION_KEYWORDS = [
  'x-altinity-discriminator',
  'x-altinity-completion',
  'x-altinity-key-completion',
  'x-altinity-snippet',
  'x-altinity-order',
  'x-altinity-deprecated',
  'x-altinity-status',
];

const HEADER = '// Generated by build/compile-spec-schema.mjs. Do not edit.\n';

async function generatedSources() {
  const schema = JSON.parse(await readFile(schemaPath, 'utf8'));
  const ajv = new Ajv2020({
    allErrors: true,
    strict: true,
    strictSchema: true,
    validateSchema: true,
    code: { esm: true, source: true, optimize: true },
  });
  for (const keyword of ANNOTATION_KEYWORDS) ajv.addKeyword(keyword);
  const validate = ajv.compile(schema);
  const standalone = standaloneCode(ajv, validate);
  const bundled = await build({
    stdin: {
      contents: standalone,
      resolveDir: root,
      sourcefile: 'query-spec-v1-standalone.js',
      loader: 'js',
    },
    bundle: true,
    format: 'esm',
    platform: 'browser',
    target: 'es2020',
    write: false,
    legalComments: 'none',
    minify: false,
  });
  return {
    [validatorPath]: HEADER + bundled.outputFiles[0].text,
    [schemaModulePath]: HEADER + 'export default ' + JSON.stringify(schema, null, 2) + ';\n',
  };
}

async function checkOrWrite(check) {
  const sources = await generatedSources();
  await mkdir(generatedDir, { recursive: true });
  let stale = false;
  for (const [path, source] of Object.entries(sources)) {
    if (check) {
      let current = '';
      try { current = await readFile(path, 'utf8'); } catch { /* missing is stale */ }
      if (current !== source) {
        console.error('stale generated schema artifact: ' + path.slice(root.length + 1));
        stale = true;
      }
    } else {
      await writeFile(path, source);
      console.log('generated ' + path.slice(root.length + 1));
    }
  }
  if (stale) process.exitCode = 1;
}

const isMain = process.argv[1] && resolve(process.argv[1]) === fileURLToPath(import.meta.url);
if (isMain) checkOrWrite(process.argv.includes('--check')).catch((error) => {
  console.error(error);
  process.exit(1);
});

export { ANNOTATION_KEYWORDS, generatedSources };
